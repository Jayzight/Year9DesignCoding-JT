
#List Of Strings
listOfStrings = ['cat','apple','dog','banana']

print (listOfStrings)
#Sorting alphabetically
listOfStrings.sort()

print (listOfStrings)

#Sorting by length
listOfStrings.sort(key=len)
print (listOfStrings)

##Reverse Alphabetical
listOfStrings.sort(reverse=True)
print (listOfStrings)